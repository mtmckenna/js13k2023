export const DEBUG = true;
export class FpsDisplay {
    div: HTMLDivElement;
    lastFrameTime: number = 0;
    frameCount: number = 0;
    fpsInterval: number = 1000;

    constructor() {
        this.div = document.createElement("div");
        this.div.id = "fpsDisplay";
        this.div.style.position = "absolute";
        this.div.style.top = "10px";
        this.div.style.left = "10px";
        this.div.style.backgroundColor = "rgba(0, 0, 0, 0.5)";
        this.div.style.color = "white";
        this.div.style.padding = "5px";
        this.div.style.fontFamily = "monospace";
        this.div.style.fontSize = "14px";
        this.div.style.borderRadius = "3px";
        this.div.style.zIndex = "1";
        this.div.textContent = "FPS: 0";
        document.body.appendChild(this.div);
    }


    update(currentTime: number) {
        const deltaTime = currentTime - this.lastFrameTime;
        this.lastFrameTime = currentTime;

        this.frameCount++;

        // Calculate the FPS and display it every 1 second
        this.fpsInterval -= deltaTime;
        if (this.fpsInterval <= 0) {
            const fps = this.frameCount;
            this.frameCount = 0;
            this.fpsInterval = 1000;
            this.div.textContent = `FPS: ${fps}`;
        }
    }
}

